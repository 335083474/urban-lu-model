import math

distances = {}
for col in range(-8, 8+1):
    for row in range(-8, 8+1):
        dist = int(round((math.sqrt((row**2+col**2)))*100))
        if dist<=800 and dist!=0:
            try:
                distances[dist].append((col, row))
            except:
                distances[dist] = [(col, row)]


